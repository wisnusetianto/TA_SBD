package com.example.sbd_modul3.model;

public class Menjual {
    private String id_penjualan;
    private String id_pembeli;
    private String id_toko;
    
    //! Setter dan Getter Properties
    public String getId_penjualan() {
    return id_penjualan;
    }
    public void setId_penjualan(String id_penjualan) {
    this.id_penjualan = id_penjualan;
    }

    public String getId_pembeli() {
    return id_pembeli;
    }
    public void setId_pembeli(String id_pembeli) {
    this.id_pembeli = id_pembeli;
    }

    public String getId_toko() {
    return id_toko;
    }
    public void setId_toko(String id_toko) {
    this.id_toko = id_toko;
    }
    
}
